#include <stdio.h>
#include <string.h>

int main(void) {
    // Объявляем две строки длиной до 10000 символов + 1 для нуль-терминатора
    char s1[10001] = {0}, s2[10001] = {0};
    
    // Считываем две строки (ограничиваем ввод 10000 символами для безопасности)
    scanf("%10000s", s1);
    scanf("%10000s", s2);

    // Вычисляем длины строк
    int n1 = strlen(s1);
    int n2 = strlen(s2);
    
    // Максимальная возможная длина совпадения - это длина меньшей строки
    int max_len = (n1 < n2) ? n1 : n2;

    /* ===== ЗАДАЧА 1: Наибольший префикс s1, являющийся суффиксом s2 ===== */
    
    int Strok1 = 0;  // Результат для первой задачи
    
    // Перебираем возможные длины от самой большой до наименьшей
    // Чем длиннее совпадение, тем лучше - поэтому идём от max_len вниз
    for (int len = max_len; len >= 1; len--) {
        int match = 1;  // Флаг совпадения (1 - совпадает, 0 - не совпадает)
        
        // Сравниваем первые len символов s1 с последними len символами s2
        // s1[i] - символ префикса s1 (идём от начала)
        // s2[n2 - len + i] - символ суффикса s2 (идём от позиции, где начинается последний кусок)
        for (int i = 0; i < len; i++) {
            if (s1[i] != s2[n2 - len + i]) {
                match = 0;  // Нашли несовпадение
                break;      // Дальше проверять смысла нет
            }
        }
        
        if (match) {        // Если все символы совпали
            Strok1 = len;   // Запоминаем длину
            break;          // Выходим, так как нашли максимальную длину
        }
    }

    /* ===== ЗАДАЧА 2: Наибольший суффикс s1, являющийся префиксом s2 ===== */
    
    int Strok2 = 0;  // Результат для второй задачи
    
    // Снова перебираем возможные длины от самой большой к наименьшей
    for (int len = max_len; len >= 1; len--) {
        int match = 1;  // Флаг совпадения
        
        // Сравниваем последние len символов s1 с первыми len символами s2
        // s1[n1 - len + i] - символ суффикса s1 (идём от позиции, где начинается последний кусок)
        // s2[i] - символ префикса s2 (идём от начала)
        for (int i = 0; i < len; i++) {
            if (s1[n1 - len + i] != s2[i]) {
                match = 0;  // Нашли несовпадение
                break;
            }
        }
        
        if (match) {        // Если все символы совпали
            Strok2 = len;   // Запоминаем длину
            break;          // Выходим, так как нашли максимальную длину
        }
    }

    // Выводим результаты: сначала длину для первого условия, затем для второго
    printf("%d %d\n", Strok1, Strok2);
    return 0;
}