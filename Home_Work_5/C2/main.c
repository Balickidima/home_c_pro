#include <stdio.h>
#include <stdlib.h>
#include <ctype.h>

int main(void) {
    // Стек для хранения операндов (максимум 100000 элементов)
    long long stack[100000];
    int top = 0;  // Указатель вершины стека (0 - стек пуст)

    char token[1000];  // Буфер для хранения текущего токена (числа или оператора)
    
    // Читаем токены, пока не достигнем конца ввода
    while (scanf("%999s", token) == 1) {
        // Если встретили точку - завершаем вычисления
        if (token[0] == '.') break;

        // Проверяем, является ли токен числом:
        // - Начинается с цифры, ИЛИ
        // - Начинается с минуса, за которым следует цифра (отрицательное число)
        if (isdigit(token[0]) || (token[0] == '-' && token[1] && isdigit(token[1]))) {
            // Преобразуем строку в число и кладём в стек
            stack[top++] = atoll(token);
        } else {
            // Токен - оператор. Извлекаем два верхних операнда из стека
            long long b = stack[--top];  // Правый операнд (первый извлечённый)
            long long a = stack[--top];  // Левый операнд (второй извлечённый)
            long long res = 0;           // Переменная для результата операции
            
            // Выполняем соответствующую арифметическую операцию
            switch (token[0]) {
                case '+': res = a + b; break;  // Сложение
                case '-': res = a - b; break;  // Вычитание
                case '*': res = a * b; break;  // Умножение
                case '/': res = a / b; break;  // Деление (целочисленное)
            }
            
            // Результат операции помещаем обратно в стек
            stack[top++] = res;
        }
    }

    // После обработки всех токенов в стеке остаётся единственное значение - результат
    printf("%lld\n", stack[0]);
    return 0;
}